    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int x = aps.Validate(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), path, jTextField5.getText()) ;
        if(x==1) JOptionPane.showMessageDialog(null, "Empty Inputs");
        else if(x==2) JOptionPane.showMessageDialog(null, "Wrong ID");
        else if(x==3) JOptionPane.showMessageDialog(null, "Numbers are Negative");
        else if(x==4) JOptionPane.showMessageDialog(null, "Check Numbers Format");
        else if(x==0) {
            JOptionPane.showMessageDialog(null, "Product Added");
            jLabel1.setText("");
            jLabel2.setText("");
            jLabel3.setText("");
            jLabel5.setText("");
            this.setVisible(false);
            this.vps.setVisible(true);
            this.vps.update();
            
        }
 
    }//GEN-LAST:event_jButton3ActionPerformed



