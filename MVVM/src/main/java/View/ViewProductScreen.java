    public void setData()
    {
        DefaultTableModel Model1=(DefaultTableModel) jTable1.getModel();
        Model1.setRowCount(0);
        for(int i=0; i<aps.getProducts().length;i++)
        {
            String data [] = {aps.getProducts()[i].getId(),
                              aps.getProducts()[i].getName(),
                              Integer.toString(aps.getProducts()[i].getPrice()),
                              Integer.toString(aps.getProducts()[i].getItems()),
                              };
            Model1.addRow(data);
        }
    }
    



